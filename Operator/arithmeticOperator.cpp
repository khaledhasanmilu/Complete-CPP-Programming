#include <iostream>
using namespace std;
int main()
{
    int a = 15;
    int b = 5;
    int sum = a + b;
    int substract = a - b;
    int multiplication = a * b;
    int division = a / b;
    int reminder = a % b;
    cout << "Sum: " << sum << "\n";
    cout << "Substract: " << substract << "\n";
    cout << "Multiplication:" << multiplication << "\n";
    cout << "Division: " << division << "\n";
    cout << "Reminder: " << reminder << "\n";
    return 0;
}