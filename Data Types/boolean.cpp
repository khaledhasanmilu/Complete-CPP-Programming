#include <iostream>
using namespace std;
int main()
{
    /*
    A boolean data type is declared with the bool keyword and can only take
     the values true or false.
    */
    bool isUniPera = false;
    bool lifeChill = true;
    cout << isUniPera << endl; // output- 1(false)
    cout << lifeChill << endl; // output- 0(true)

    return 0;
}