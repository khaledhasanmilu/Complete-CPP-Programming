#include <iostream>
using namespace std;
int main()
{
    // input part
    int n, m;
    cout << "Enter first number: ";
    cin >> n;
    cout << "Enter Second number: ";
    cin >> m;
    cout << "Your Sum : " << n + m << endl;
    cout << "Your Multiplication is : " << n * m << endl;
    cout << "Your Subtract is: " << n - m << endl;
    cout << "Your Division : " << n / m << endl;

    return 0;
}